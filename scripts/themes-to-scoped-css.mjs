#!/usr/bin/env node
/**
 * Generates docs-only artifacts so the docs site can render every theme
 * simultaneously without iframes or stylesheet swapping.
 *
 * Outputs:
 *   - docs-nextra/themes-scoped.css   one CSS file emitting each theme's
 *                                     tokens under [data-rk-card-theme='<id>']
 *                                     selectors, so gallery cards can scope
 *                                     themes per-card via the cascade.
 *   - docs-nextra/themes-data.json    a JSON list of { id, displayName, note,
 *                                     light, dark } for the gallery + dropdown.
 *
 * Sources of truth:
 *   - ai/contracts/index.json                  theme registry (enumeration)
 *   - ai/contracts/themes/<id>-theme.contract.json   displayName
 *   - src/themes/<id>.css                      token values
 */

import { readFileSync, writeFileSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const REPO_ROOT = resolve(dirname(fileURLToPath(import.meta.url)), '..');

const NOTES = {
  atmospheric: 'airy and tinted',
  brutalist: 'sharp and loud',
  carbon: 'ibm-style precision',
  corporate: 'enterprise tone',
  default: 'neutral baseline',
  dense: 'data-heavy admin',
  forest: 'organic contrast',
  midnight: 'high contrast cool',
  monday: 'monday.com-inspired vibrancy',
  ocean: 'clean and vibrant',
  polaris: 'shopify-inspired clarity',
  sand: 'warm palette',
  slate: 'neutral utility',
  sunset: 'expressive blend',
};

// Tokens we surface as previews in the gallery card. Keep in sync with
// ThemeGallery.tsx — any addition here must be consumed there.
const PREVIEW_TOKENS = [
  'background',
  'foreground',
  'card',
  'primary',
  'primary-foreground',
  'accent',
  'accent-foreground',
  'accent-2',
  'accent-3',
  'accent-4',
  'success',
  'warning',
  'info',
  'destructive',
  'border',
  'muted',
  'muted-foreground',
  'radius',
  'chart-1',
  'chart-2',
  'chart-3',
  'chart-4',
  'chart-5',
  'chart-6',
];

function readJson(relPath) {
  return JSON.parse(readFileSync(resolve(REPO_ROOT, relPath), 'utf8'));
}

function parseThemeCss(cssText) {
  const blockRe = /([:.][\w-]+)\s*\{([^}]*)\}/g;
  const out = { light: {}, dark: {} };
  let match;
  while ((match = blockRe.exec(cssText)) !== null) {
    const selector = match[1];
    const body = match[2];
    const bucket =
      selector === ':root' ? 'light' : selector === '.dark' ? 'dark' : null;
    if (!bucket) continue;

    const declRe = /(--[\w-]+)\s*:\s*([^;]+);/g;
    let decl;
    while ((decl = declRe.exec(body)) !== null) {
      out[bucket][decl[1]] = decl[2].trim();
    }
  }
  return out;
}

function pickPreviewTokens(tokenMap) {
  const out = {};
  for (const name of PREVIEW_TOKENS) {
    const key = `--rk-${name}`;
    if (tokenMap[key] !== undefined) out[name] = tokenMap[key];
  }
  return out;
}

function emitDeclarations(tokenMap, indent = '  ') {
  return Object.entries(tokenMap)
    .map(([name, value]) => `${indent}${name}: ${value};`)
    .join('\n');
}

const registry = readJson('ai/contracts/index.json');
const themes = registry.themes ?? [];

const cssLines = [
  '/* Auto-generated by scripts/themes-to-scoped-css.mjs. Do not edit. */',
  '/* Source: src/themes/*.css + ai/contracts/themes/*.contract.json */',
  '',
];
const data = [];

for (const entry of themes) {
  const cssText = readFileSync(resolve(REPO_ROOT, entry.cssPath), 'utf8');
  const tokens = parseThemeCss(cssText);
  const contract = readJson(entry.contractPath);

  cssLines.push(`[data-rk-card-theme='${entry.id}'] {`);
  cssLines.push(emitDeclarations(tokens.light));
  cssLines.push('}');
  if (Object.keys(tokens.dark).length > 0) {
    cssLines.push(`[data-rk-card-theme='${entry.id}'].dark {`);
    cssLines.push(emitDeclarations(tokens.dark));
    cssLines.push('}');
  }
  cssLines.push('');

  data.push({
    id: entry.id,
    displayName: contract.displayName ?? entry.id,
    note: NOTES[entry.id] ?? '',
    light: pickPreviewTokens(tokens.light),
    dark: pickPreviewTokens(tokens.dark),
  });
}

const cssPath = resolve(REPO_ROOT, 'docs-nextra/themes-scoped.css');
const jsonPath = resolve(REPO_ROOT, 'docs-nextra/themes-data.json');

writeFileSync(cssPath, cssLines.join('\n'));
writeFileSync(jsonPath, `${JSON.stringify(data, null, 2)}\n`);

console.log(
  `themes:codegen wrote ${themes.length} themes → docs-nextra/themes-scoped.css + themes-data.json`,
);
